// Helper to find the nearest bead color using DeltaE 2000
export function findNearestBeadColor(r: number, g: number, b: number, palette: '221' | '291' = '221'): BeadColor {
  const targetLab = rgbToLab(r, g, b);
  let minDistance = Infinity;
  const colors = palette === '221' ? BEAD_COLORS_221 : BEAD_COLORS_291;
  let nearestColor = colors[0];

  // Skin tone detection
  const isSkinTone = targetLab.l > 50 && targetLab.l < 90 && targetLab.a > 5 && targetLab.a < 35 && targetLab.b > 5 && targetLab.b < 35;

  for (const color of colors) {
    let distance = deltaE2000(targetLab, color.lab);
    
    if (isSkinTone) {
      distance /= 2;
    }

    if (distance < minDistance) {
      minDistance = distance;
      nearestColor = color;
    }
  }

  return nearestColor;
}
